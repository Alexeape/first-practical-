
public class Converter {
    void findDistance(int startStep, double Distance) {

        System.out.println("Всего пройдено " + startStep * Distance / 100000 + " км");

    }
    void findCalories(int startStep, double Calories) {
        System.out.println("Количество сожжённых калорий: " + startStep * Calories / 100000);

    }

}

